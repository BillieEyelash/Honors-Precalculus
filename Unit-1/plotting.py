import matplotlib.pyplot as plt

# x axis values
x = [1, 2, 3]

# corresponding y axis values
y = [2, 4, 1]

# plotting the points
plt.plot(x,y)

# naming the x axis
plt.xlabel('x - axis')

# naming the y axis
plt.ylabel('y - axis')

# assign a title to the graph
plt.title('My first graph!')

# display the plot
plt.show()
