score = int(input("Enter your score on the test: "))
if score > 93:
    print("A")
elif score > 90:
    print("A-")
elif score > 87:
    print("B+")
elif score > 83:
    print("B")
elif score > 80:
    print("B-")
elif score > 77:
    print("C+")
elif score > 73:
    print("C")
elif score > 70:
    print("C-")
else:
    print("E")
